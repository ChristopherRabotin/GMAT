//                        "TAIModJulian");
//         throw sse;
//      }
//      twoBodyFormat = value;
//      return true;
//   }
//   if (id == TWO_BODY_STATE_TYPE)
//   {
//      #ifdef DEBUG_TWO_BODY
//         MessageInterface::ShowMessage(
//         "In CB::SetString (TWO_BODY_STATE_TYPE) with id = %d, and value = %s\n",
//         id, value.c_str());
//      #endif
//      if (value != "Keplerian")
//      {
//         SolarSystemException sse;
