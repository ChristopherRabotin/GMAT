{
}


//------------------------------------------------------------------------------
//  GmatBase* Clone() const
//------------------------------------------------------------------------------
/**
 * Clone of the Atan operation.
 *
 * @return clone of the Atan operation.
 *
 */
//------------------------------------------------------------------------------
GmatBase* Atan::Clone() const
{
   return (new Atan(*this));
}


//------------------------------------------------------------------------------
// Real Evaluate()
//------------------------------------------------------------------------------
/**
 * @return the Atan of left node
 *
 */
//------------------------------------------------------------------------------
Real Atan::Evaluate()
{
   return GmatMathUtil::ATan(leftNode->Evaluate());
}
