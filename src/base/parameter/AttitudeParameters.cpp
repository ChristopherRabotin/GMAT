{
   if (this != &right)
      AttitudeReal::operator=(right);

   return *this;
}
//------------------------------------------------------------------------------
// ~DCM11()
//------------------------------------------------------------------------------
DCM11::~DCM11()
{
}
//------------------------------------------------------------------------------
// bool Evaluate()
//------------------------------------------------------------------------------
bool DCM11::Evaluate()
{
   mRealValue = AttitudeData::GetAttitudeReal(DCM1_1);
   
    
   if (mRealValue == AttitudeData::ATTITUDE_REAL_UNDEFINED)
      return false;
   else
      return true;
}
//------------------------------------------------------------------------------
// GmatBase* DCM11::Clone(void) const
//------------------------------------------------------------------------------
